_DEFAULT_NOTE = EpochNote(
    title="Эпоха",
    summary="Для этой эпохи ещё не добавлена игровая аннотация.",
    bullets=(),
    visual_hint="визуализация будет уточнена позже",
)


def get_epoch_note(stage_id: str | None) -> EpochNote:
    """Return local annotation for a stage id."""

    if stage_id is None:
        return _DEFAULT_NOTE
    return _EPOCH_NOTES.get(stage_id, _DEFAULT_NOTE)
